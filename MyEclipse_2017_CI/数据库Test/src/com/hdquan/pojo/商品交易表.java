package com.hdquan.pojo;

import java.util.Date;

public class 商品交易表 {
	String 交易流水号;
	String 商品编号;
	String 商品名称;
	String 会员卡号;
	Date 交易日期;
	String 交易数量;
	String 金额;
	String 商品总金额;
	public String get交易流水号() {
		return 交易流水号;
	}
	public void set交易流水号(String 交易流水号) {
		this.交易流水号 = 交易流水号;
	}
	public String get商品编号() {
		return 商品编号;
	}
	public void set商品编号(String 商品编号) {
		this.商品编号 = 商品编号;
	}
	public String get商品名称() {
		return 商品名称;
	}
	public void set商品名称(String 商品名称) {
		this.商品名称 = 商品名称;
	}
	public String get会员卡号() {
		return 会员卡号;
	}
	public void set会员卡号(String 会员卡号) {
		this.会员卡号 = 会员卡号;
	}
	public Date get交易日期() {
		return 交易日期;
	}
	public void set交易日期(Date 交易日期) {
		this.交易日期 = 交易日期;
	}
	public String get交易数量() {
		return 交易数量;
	}
	public void set交易数量(String 交易数量) {
		this.交易数量 = 交易数量;
	}
	public String get金额() {
		return 金额;
	}
	public void set金额(String 金额) {
		this.金额 = 金额;
	}
	public String get商品总金额() {
		return 商品总金额;
	}
	public void set商品总金额(String 商品总金额) {
		this.商品总金额 = 商品总金额;
	}
	
}	
