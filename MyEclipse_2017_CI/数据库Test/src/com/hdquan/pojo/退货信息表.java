package com.hdquan.pojo;

import java.util.Date;

public class 退货信息表 {
	String 交易流水号;
	String 商品编号;
	String 退货金额;
	String 退货日期;
	String 退货数量;
	public String get交易流水号() {
		return 交易流水号;
	}
	public void set交易流水号(String 交易流水号) {
		this.交易流水号 = 交易流水号;
	}
	public String get商品编号() {
		return 商品编号;
	}
	public void set商品编号(String 商品编号) {
		this.商品编号 = 商品编号;
	}
	public String get退货金额() {
		return 退货金额;
	}
	public void set退货金额(String 退货金额) {
		this.退货金额 = 退货金额;
	}
	public String get退货日期() {
		return 退货日期;
	}
	public void set退货日期(String 退货日期) {
		this.退货日期 = 退货日期;
	}
	public String get退货数量() {
		return 退货数量;
	}
	public void set退货数量(String 退货数量) {
		this.退货数量 = 退货数量;
	}
	
}
